package Day17.Junit;

public class Calculator {

    /**
     * Adding
     * @param a
     * @param b
     * @return
     */
    public int add(int a,int b){
        return a+b;
    }

    /**
     * subtracting
     * @param a
     * @param b
     * @return
     */
    public int sub(int a,int b){
        return a-b;
    }

}
